def read_key_order(path):
    root = ET.parse(path).getroot()
    return [child.tag for child in root if isinstance(child.tag, str)]


def write_language_data(path, entries):
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = ['<?xml version="1.0" encoding="utf-8"?>', "<LanguageData>"]
    for key, value in entries:
        text = escape(value)
        lines.append(f"  <{key}>{text}</{key}>")
    lines.append("</LanguageData>")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_keyed(source_path, dest_path, translations):
    keys = read_key_order(source_path)
    missing = [key for key in keys if key not in translations]
    extra = [key for key in translations if key not in keys]
    if missing or extra:
        raise RuntimeError(f"{dest_path.name}: missing={missing}, extra={extra}")
    write_language_data(dest_path, [(key, translations[key]) for key in keys])


def extract_def_keys(paths, fields, list_fields):
    keys = []
    for path in paths:
        root = ET.parse(path).getroot()
        for element in root:
            if not isinstance(element.tag, str):
                continue
            def_name = element.findtext("defName")
            if not def_name:
                continue
            for child in element:
                if child.tag in fields and (child.text or "").strip():
                    keys.append(f"{def_name}.{child.tag}")
                if child.tag in list_fields:
                    for idx, item in enumerate(child.findall("li")):
                        if (item.text or "").strip():
                            keys.append(f"{def_name}.{child.tag}.{idx}")
    return keys


def write_def_injected(source_paths, dest_path, translations, fields=("label", "description", "desc"), list_fields=()):
    keys = extract_def_keys(source_paths, set(fields), set(list_fields))
    missing = [key for key in keys if key not in translations]
    extra = [key for key in translations if key not in keys]
    if missing or extra:
        raise RuntimeError(f"{dest_path.name}: missing={missing}, extra={extra}")
    write_language_data(dest_path, [(key, translations[key]) for key in keys])


def write_patch_notes(source_dir, dest_path, translations):
    source_paths = sorted(source_dir.glob("PatchNoteDefs_v*.xml"))
    keys = extract_def_keys(source_paths, {"label", "description"}, {"patchNoteLines"})
    missing = [key for key in keys if key not in translations]
    extra = [key for key in translations if key not in keys]
    if missing or extra:
        raise RuntimeError(f"{dest_path.name}: missing={missing}, extra={extra}")

    entries = []
    concrete_defs = []
    for path in source_paths:
        root = ET.parse(path).getroot()
        for element in root:
            def_name = element.findtext("defName")
            if not def_name:
                continue
            concrete_defs.append(def_name)
            for child in element:
                if child.tag in {"label", "description"} and (child.text or "").strip():
                    key = f"{def_name}.{child.tag}"
                    entries.append((key, translations[key]))
                if child.tag == "patchNoteLines":
                    for idx, item in enumerate(child.findall("li")):
                        if (item.text or "").strip():
                            key = f"{def_name}.patchNoteLines.{idx}"
                            entries.append((key, translations[key]))
    for def_name in concrete_defs:
        for suffix, value in COMMON_PATCH_NOTES.items():
            entries.append((f"{def_name}.{suffix}", value))
    write_language_data(dest_path, entries)


